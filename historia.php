          <p class="parrafo">En cumplimiento de lo establecido por la Ley &nbsp;No.181 de 18 de enero 1995, en su Art&iacute;culo 65, que ordena que las Juntas Administradoras Seccionales de Deportes, se incorporar&iacute;an al respectivo Departamento como entes departamentales del Deporte y la Recreaci&oacute;n, de conformidad con las Ordenanzas que para tal fin expidieran las Asambleas Departamentales. Es as&iacute; como en el a&ntilde;o 1996, mediante Ordenanza No. 093 de 23 Diciembre 1996, la Honorable Asamblea Departamental, le otorg&oacute; al se&ntilde;or &nbsp;Gobernador de Santander de la &eacute;poca, Dr. MARIO CAMACHO PRADA, precisas facultades para que mediante Decreto cree el Instituto Departamental de Recreaci&oacute;n y Deportes (I.D.R.D.).</p>
          <p class="parrafo"><br />
            Mediante Decreto No. 054 de 18 febrero 1997, se crea el Instituto Departamental de Recreaci&oacute;n y Deportes (I.D.R.D.), como entidad descentralizada del orden Departamental, clasificado como establecimiento p&uacute;blico, con personer&iacute;a Jur&iacute;dica, autonom&iacute;a administrativa y patrimonio independiente, el cual hace parte integral del Sistema Nacional del Deporte. Este mismo Decreto se&ntilde;ala como objetivos de la entidad, Coordinar el Plan Nacional del Deporte, la Recreaci&oacute;n y la Educaci&oacute;n F&iacute;sica en los t&eacute;rminos de la Ley 181 de 1995, la contribuci&oacute;n al desarrollo de la educaci&oacute;n familiar escolar y extraescolar de la ni&ntilde;ez y de la juventud para que utilicen el tiempo libre, el deporte y la recreaci&oacute;n como elementos fundamentales en su proceso de formaci&oacute;n personal como comunitario; la estimulaci&oacute;n de la participaci&oacute;n ciudadana y la integraci&oacute;n funcional en los t&eacute;rminos de la Constituci&oacute;n Pol&iacute;tica, la Ley y dem&aacute;s normas que regulen la pr&aacute;ctica deportiva, la educaci&oacute;n f&iacute;sica y el aprovechamiento del tiempo libre. En ese sentido orden&oacute; orientar sus recursos buscando adem&aacute;s, su correcta destinaci&oacute;n, la eficiencia, econom&iacute;a y equidad. </p>
          <p class="parrafo"><br />
            Posteriormente mediante Decreto No. 0225 de 22 julio 1997, se modifica el Decreto No. 0054 de 1997,&nbsp; cambia su naturaleza jur&iacute;dica dej&aacute;ndolo adscrito a la Secretar&iacute;a de Educaci&oacute;n Departamental; igualmente &nbsp;incorpora a la Junta Administradora Seccional de Deporte, creada por la Ley No. 049 de 1983.&nbsp; Establece como domicilio legal del I.D.R.D. la ciudad de Bucaramanga y adiciona nueva funciones. </p>
          <p class="parrafo"><br />
            El 11 de junio de 1999, se determin&oacute; en Acta cuales funcionarios de la Junta Administradora Seccional de Deportes de Santander (COLDEPORTES SANTANDER) se incorporar&iacute;an a partir del 01 de Julio de 1999 al I.D.R.D. De otro lado se puede concluir que de esta fecha en adelante es cuando el Instituto toma vida administrativa y presupuestal como &uacute;nico ente del Deporte y la Recreaci&oacute;n en el Departamento. </p>
          <p class="parrafo">Con la Ordenanza No. 029 de 03 de agosto 1999, la Honorable Asamblea Departamental, &nbsp;modifica la sigla del Instituto Departamental de Recreaci&oacute;n y Deporte &ldquo;I.D.R.D.&rdquo; y adopta &nbsp;la de Instituto Departamental de Recreaci&oacute;n y Deportes de Santander &nbsp;&ldquo;INDERSANTANDER&rdquo;, con el fin de determinar la entidad territorial que representa el Instituto, cuando de participaciones a nivel nacional e internacional se trate. &nbsp;Igualmente al incluir en la sigla el nombre del Departamento de Santander, se le dio la importancia que este merece, la identidad y sentido de pertenencia que debe estar presente en cada uno de los Santandereanos. </p>
          <p class="parrafo">La creaci&oacute;n del INDERSANTANDER, est&aacute; sustentada como ente Departamental encargado de coordinar, controlar y ejecutar lo relativo a los planes y programas establecidos en materia deportiva y recreativa a nivel regional.</p>
          <p class="parrafo">Mediante Acuerdo No. 003 de 23 de Enero&nbsp; 2000, la Junta Directiva del Instituto adopta la planta de personal con doce (12) &nbsp;cargos as&iacute;: 01 Director General, 01 Asesor, 04 Profesionales Universitarios, 04 Auxiliares Administrativos, 01 T&eacute;cnico. Seguidamente &nbsp;mediante Acuerdo No. 006 de 07 abril 2000, la Junta Directiva modifica la planta de personal y la amplia en &nbsp;dos (2) cargos: 01 profesional y 01 auxiliar administrativo, en ese mismo Acuerdo se ordena al Director General distribuir los cargos de planta mediante acto administrativo y ubicar al personal teniendo en cuenta la estructura, las necesidades del servicio, planes y programas tratados por la entidad. Posteriormente mediante Resoluci&oacute;n No. 012 de 20 de enero de 2003, previas facultades otorgadas por la Junta Directiva, el Director de la &eacute;poca crea el cargo de Control Interno. En el a&ntilde;o 2005, mediante Resoluci&oacute;n No. 172 de &nbsp;2005 proferida por la Directora de la &eacute;poca, la planta de personal sufre otra modificaci&oacute;n, en esta ocasi&oacute;n la Junta Directiva le aprueba la supresi&oacute;n de cargo Profesional Universitario - Almac&eacute;n y ordena la creaci&oacute;n del empleo T&eacute;cnico Administrativo para el ejercicio de esta funci&oacute;n. Por &uacute;ltimo mediante Acuerdo No. 003 de 23 de Enero 2013, se modifica la planta de cargos a 17 funcionarios creando el cargo de un Profesional Universitario para la Coordinaci&oacute;n del &aacute;rea misional Deporte de Alto Rendimiento.&nbsp;&nbsp;            </p>
